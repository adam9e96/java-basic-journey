package ch02_Variables.sec05_string;

public class StringExample02 {
    public static void main(String[] args) {
        // String은 참조 변수(Reference Variable)임
        // 참조 변수 종류: 객체, 배열, 문자열등이 있음
        String greetString = "Hello";
        System.out.println("문자열 길이: "+greetString.length()); // 기본 타입과는 다르게 기본 제공하는 메서드가 있다.
    }

}
